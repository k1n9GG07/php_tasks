<?php
/**
 * Пример 1: Основы синтаксиса PHP
 * 
 * Этот файл демонстрирует базовые элементы PHP-кода
 */

// Вывод текста в браузер
echo "Привет, мир!<br>";

// Вывод с использованием print
print "Это тоже работает!<br>";

// Вывод HTML-тегов
echo "<h1>Заголовок первого уровня</h1>";
echo "<p>Это параграф текста.</p>";

// Переменные начинаются с символа $
$имя = "Андрей";
$возраст = 33;

// Вывод переменных
echo "Меня зовут $имя, мне $возраст года.<br>";

// Конкатенация (объединение) строк
$приветствие = "Привет, " . $имя . "!<br>";
echo $приветствие;

// Математические операции
echo "<h2>Математические операции</h2>";
$число1 = 10;
$число2 = 5;
echo "Допустим, у нас есть два числа: $число1 и $число2.<br>";

echo "Сложение: " . ($число1 + $число2) . "<br>";
echo "Вычитание: " . ($число1 - $число2) . "<br>";
echo "Умножение: " . ($число1 * $число2) . "<br>";
echo "Деление: " . ($число1 / $число2) . "<br>";
echo "Остаток от деления: " . ($число1 % $число2) . "<br>";

// Условные операторы
echo "<h2>Условные операторы</h2>";
$температура = 20;

if ($температура > 25) {
    echo "Жарко!<br>";
} elseif ($температура > 15) {
    echo "Тепло!<br>";
} else {
    echo "Прохладно!<br>";
}

// Цикл for
echo "<h2>Цикл от 1 до 5:</h2>";
for ($i = 1; $i <= 5; $i++) {
    echo "Число: $i<br>";
}

// Цикл while
echo "<h2>Обратный отсчет:</h2>";
$счетчик = 5;
while ($счетчик > 0) {
    echo "$счетчик...<br>";
    $счетчик--;
}
echo "Старт!<br>";

// Типы данных
echo "<h2>Типы данных:</h2>";
$строка = "Это строка";
$число = 42;
$дробное = 3.14;
$логическое = true;
$массив = [1, 2, 3];

echo "Строка: $строка (тип: " . gettype($строка) . ")<br>";
echo "Число: $число (тип: " . gettype($число) . ")<br>";
echo "Дробное: $дробное (тип: " . gettype($дробное) . ")<br>";
echo "Логическое: " . ($логическое ? 'true' : 'false') . " (тип: " . gettype($логическое) . ")<br>";
echo "Массив: " . print_r($массив, true) . " (тип: " . gettype($массив) . ")<br>";

?>

