<?php
/**
 * Пример 2: Работа с переменными
 * 
 * Демонстрация различных типов переменных и операций с ними
 */

echo "<h1>Работа с переменными в PHP</h1>";

// ============================================
// Строковые переменные
// ============================================
echo "<h2>Строковые переменные:</h2>";

$имя = "Анна";
$фамилия = "Петрова";
$полное_имя = $имя . " " . $фамилия; // Конкатенация

echo "Имя: $имя<br>";
echo "Фамилия: $фамилия<br>";
echo "Полное имя: $полное_имя<br>";

// Функции для работы со строками
echo "<h3>Функции для работы со строками:</h3>";
echo "Длина строки '$полное_имя': " . strlen($полное_имя) . " символов<br>";
echo "В верхнем регистре: " . strtoupper($полное_имя) . "<br>";
echo "В нижнем регистре: " . strtolower($полное_имя) . "<br>";
echo "Первая буква заглавная: " . ucfirst($полное_имя) . "<br>";
echo "Замена: " . str_replace("Анна", "Мария", $полное_имя) . "<br>";

// ============================================
// Числовые переменные
// ============================================
echo "<h2>Числовые переменные:</h2>";

$цена = 1000;
$скидка = 15; // в процентах
$итоговая_цена = $цена * (1 - $скидка / 100);

echo "Цена: $цена руб.<br>";
echo "Скидка: $скидка%<br>";
echo "Итоговая цена: " . round($итоговая_цена, 2) . " руб.<br>";

// Математические функции
echo "<h3>Математические функции:</h3>";
$число = 16;
echo "Квадратный корень из $число: " . sqrt($число) . "<br>";
echo "Возведение в степень (2^8): " . pow(2, 8) . "<br>";
echo "Округление 3.7: " . round(3.7) . "<br>";
echo "Округление вверх 3.2: " . ceil(3.2) . "<br>";
echo "Округление вниз 3.8: " . floor(3.8) . "<br>";

// ============================================
// Логические переменные
// ============================================
echo "<h2>Логические переменные:</h2>";

$есть_права = true;
$возраст = 18;
$может_голосовать = ($возраст >= 18) && $есть_права;

echo "Возраст: $возраст<br>";
echo "Есть права: " . ($есть_права ? 'Да' : 'Нет') . "<br>";
echo "Может голосовать: " . ($может_голосовать ? 'Да' : 'Нет') . "<br>";

// Логические операторы
echo "<h3>Логические операторы:</h3>";
$a = true;
$b = false;
echo "a = true, b = false<br>";
echo "a && b (И): " . ($a && $b ? 'true' : 'false') . "<br>";
echo "a || b (ИЛИ): " . ($a || $b ? 'true' : 'false') . "<br>";
echo "!a (НЕ): " . (!$a ? 'true' : 'false') . "<br>";

// ============================================
// Константы
// ============================================
echo "<h2>Константы:</h2>";

define("ПИ", 3.14159);
define("НАЗВАНИЕ_САЙТА", "Мой сайт");

echo "Число ПИ: " . ПИ . "<br>";
echo "Название сайта: " . НАЗВАНИЕ_САЙТА . "<br>";

// Современный способ (PHP 5.3+)
const СТОИМОСТЬ_ДОСТАВКИ = 300;
echo "Стоимость доставки: " . СТОИМОСТЬ_ДОСТАВКИ . " руб.<br>";

// ============================================
// Проверка типа переменной
// ============================================
echo "<h2>Проверка типа переменной:</h2>";

$переменные = [
    "строка" => "Привет",
    "число" => 42,
    "дробное" => 3.14,
    "логическое" => true,
    "массив" => [1, 2, 3],
    "null" => null
];

foreach ($переменные as $название => $значение) {
    echo "$название: ";
    var_dump($значение);
    echo "<br>";
}

// ============================================
// Преобразование типов
// ============================================
echo "<h2>Преобразование типов:</h2>";

$строка_число = "123";
$число_из_строки = (int)$строка_число;
echo "Строка '$строка_число' преобразована в число: $число_из_строки<br>";

$число_строка = 456;
$строка_из_числа = (string)$число_строка;
echo "Число $число_строка преобразовано в строку: '$строка_из_числа'<br>";

$логическое_из_числа = (bool)1;
echo "Число 1 преобразовано в логическое: " . ($логическое_из_числа ? 'true' : 'false') . "<br>";

