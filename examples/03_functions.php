<?php
/**
 * Пример 3: Функции в PHP
 * 
 * Демонстрация создания и использования функций
 */

echo "<h1>Функции в PHP</h1>";

// ============================================
// Простая функция без параметров
// ============================================
echo "<h2>Простая функция:</h2>";

function приветствие() {
    return "Привет из функции!";
}

echo приветствие() . "<br>";

// ============================================
// Функция с параметрами
// ============================================
echo "<h2>Функция с параметрами:</h2>";

function приветствие_с_именем($имя) {
    return "Привет, $имя!";
}

echo приветствие_с_именем("Иван") . "<br>";
echo приветствие_с_именем("Мария") . "<br>";

// ============================================
// Функция с несколькими параметрами
// ============================================
echo "<h2>Функция с несколькими параметрами:</h2>";

function представление($имя, $возраст, $город) {
    return "Меня зовут $имя, мне $возраст лет, я из города $город.";
}

echo представление("Алексей", 28, "Москва") . "<br>";

// ============================================
// Функция с параметрами по умолчанию
// ============================================
echo "<h2>Параметры по умолчанию:</h2>";

function приветствие_с_языком($имя, $язык = "русский") {
    $приветствия = [
        "русский" => "Привет",
        "английский" => "Hello",
        "испанский" => "Hola"
    ];
    
    $приветствие = $приветствия[$язык] ?? "Привет";
    return "$приветствие, $имя!";
}

echo приветствие_с_языком("Иван") . "<br>";
echo приветствие_с_языком("John", "английский") . "<br>";
echo приветствие_с_языком("Juan", "испанский") . "<br>";

// ============================================
// Функция с возвращаемым значением
// ============================================
echo "<h2>Функции с возвращаемым значением:</h2>";

function сложить($a, $b) {
    return $a + $b;
}

function умножить($a, $b) {
    return $a * $b;
}

$результат1 = сложить(5, 3);
$результат2 = умножить(4, 7);

echo "5 + 3 = $результат1<br>";
echo "4 × 7 = $результат2<br>";

// ============================================
// Функция с типизацией параметров
// ============================================
echo "<h2>Типизированные функции:</h2>";

function вычислить_площадь_круга(float $радиус): float {
    return pi() * $радиус * $радиус;
}

function проверить_возраст(int $возраст): string {
    if ($возраст >= 18) {
        return "Совершеннолетний";
    }
    return "Несовершеннолетний";
}

echo "Площадь круга с радиусом 5: " . вычислить_площадь_круга(5) . "<br>";
echo "Возраст 20: " . проверить_возраст(20) . "<br>";
echo "Возраст 15: " . проверить_возраст(15) . "<br>";

// ============================================
// Анонимные функции (замыкания)
// ============================================
echo "<h2>Анонимные функции:</h2>";

$умножить_на_два = function($число) {
    return $число * 2;
};

echo "5 × 2 = " . $умножить_на_два(5) . "<br>";

// Использование в массиве
$числа = [1, 2, 3, 4, 5];
$удвоенные = array_map($умножить_на_два, $числа);
echo "Удвоенные числа: " . implode(", ", $удвоенные) . "<br>";

// ============================================
// Стрелочные функции (PHP 7.4+)
// ============================================
echo "<h2>Стрелочные функции:</h2>";

$возвести_в_квадрат = fn($x) => $x * $x;
echo "5² = " . $возвести_в_квадрат(5) . "<br>";

// ============================================
// Рекурсивная функция
// ============================================
echo "<h2>Рекурсивная функция (факториал):</h2>";

function факториал(int $n): int {
    if ($n <= 1) {
        return 1;
    }
    return $n * факториал($n - 1);
}

echo "Факториал 5 = " . факториал(5) . "<br>";
echo "Факториал 7 = " . факториал(7) . "<br>";

// ============================================
// Встроенные функции PHP
// ============================================
echo "<h2>Встроенные функции PHP:</h2>";

$строка = "  Привет, мир!  ";
echo "Оригинал: '$строка'<br>";
echo "Trim (убрать пробелы): '" . trim($строка) . "'<br>";
echo "Длина: " . strlen($строка) . "<br>";
echo "Верхний регистр: " . strtoupper($строка) . "<br>";
echo "Подстрока (0-6): " . substr($строка, 0, 6) . "<br>";

$массив = [3, 1, 4, 1, 5, 9, 2, 6];
echo "Массив: " . implode(", ", $массив) . "<br>";
echo "Количество элементов: " . count($массив) . "<br>";
echo "Максимум: " . max($массив) . "<br>";
echo "Минимум: " . min($массив) . "<br>";
sort($массив);
echo "Отсортированный: " . implode(", ", $массив) . "<br>";

